#define KERNEL_DATA_SEG $16

.macro Handler IntrNum,EndFunc,HasErrorCode
.global AsmHandler\IntrNum
AsmHandler\IntrNum:
	cli
.if \HasErrorCode
.else
	pushl $0    /*若没有ErrorCode 那么ErrorCode就应该是0*/ 
.endif
	pushl $\IntrNum   /*把中断号压栈*/ 
	pushal
	
	pushl %ds
	pushl %es
	pushl %fs
	pushl %gs

	xorl %ebx,%ebx
	movw %ds,%ax
	movzx %ax,%eax
	bt $2,%eax
	setnc %bl
	pushl %ebx    /*Kernel or User?*/ 


	movw KERNEL_DATA_SEG,%ax
	movw %ax,%ds
	movw %ax,%es
	movw %ax,%ss

	push %esp
	call \EndFunc
	addl $4,%esp

	addl $4,%esp

	popl %gs
	popl %fs
	popl %es
	popl %ds
	popal

	addl $8,%esp
	
	iret
.endm
.extern CHandler

